package wxrmq.domain;

public class TagValue {
	private String tag;
	private String subTag;
	private Integer y;
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	public Integer getY() {
		return y;
	}
	public void setY(Integer value) {
		this.y = value;
	}
	public String getSubTag() {
		return subTag;
	}
	public void setSubTag(String subTag) {
		this.subTag = subTag;
	}
}
